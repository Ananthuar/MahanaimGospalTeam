// Handle language switching
document.addEventListener("DOMContentLoaded", () => {
    const currentLang = localStorage.getItem('mahanaim_lang') || 'en';
    setLanguage(currentLang);

    // Setup toggle button listeners if any
    const toggleBtn = document.getElementById('langToggle');
    if (toggleBtn) {
        toggleBtn.textContent = currentLang === 'en' ? 'ML' : 'EN';
        toggleBtn.addEventListener('click', () => {
            const newLang = localStorage.getItem('mahanaim_lang') === 'en' ? 'ml' : 'en';
            setLanguage(newLang);
            toggleBtn.textContent = newLang === 'en' ? 'ML' : 'EN';
        });
    }

    // Creative social links interaction
    const socialLinks = document.querySelectorAll('.social-links a[href="#"]');
    socialLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();

            const lang = localStorage.getItem('mahanaim_lang') || 'en';
            const badgeText = lang === 'en' ? 'Coming Soon!' : 'ഉടൻ വരുന്നു!';

            const rect = link.getBoundingClientRect();
            const badge = document.createElement('div');
            badge.textContent = badgeText;
            badge.style.position = 'fixed';
            badge.style.top = `${rect.top - 20}px`;
            badge.style.left = `${rect.left + rect.width / 2}px`;
            badge.style.transform = 'translateX(-50%)';
            badge.style.background = 'var(--grad-vibrant)';
            badge.style.color = '#fff';
            badge.style.padding = '0.4rem 0.8rem';
            badge.style.borderRadius = 'var(--radius-sm)';
            badge.style.fontSize = '0.85rem';
            badge.style.fontWeight = 'bold';
            badge.style.pointerEvents = 'none';
            badge.style.zIndex = '9999';
            badge.style.boxShadow = '0 4px 12px rgba(255, 0, 127, 0.4)';
            badge.style.transition = 'all 0.6s cubic-bezier(0.25, 1, 0.5, 1)';
            badge.style.opacity = '1';

            document.body.appendChild(badge);

            link.style.transition = 'transform 0.2s';
            link.style.transform = 'scale(0.8) rotate(-15deg)';
            setTimeout(() => { link.style.transform = ''; }, 200);

            requestAnimationFrame(() => {
                requestAnimationFrame(() => {
                    badge.style.top = `${rect.top - 60}px`;
                    badge.style.opacity = '0';
                });
            });

            setTimeout(() => { badge.remove(); }, 600);
        });
    });
});

function setLanguage(lang) {
    localStorage.setItem('mahanaim_lang', lang);
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // If it's an input or textarea with placeholder, update placeholder instead
            if ((el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') && el.hasAttribute('placeholder')) {
                el.setAttribute('placeholder', translations[lang][key]);
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });
}
